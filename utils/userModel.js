class User {
 constructor(name, image, bio, link, reposCount, followers, following, repos) {
  this.name = name;
  this.bio = bio;
  this.image = image;
  this.link = link;
  this.reposCount = reposCount;
  this.followers = followers;
  this.following = following;
  this.repos = repos;
 }
}
export default User;
